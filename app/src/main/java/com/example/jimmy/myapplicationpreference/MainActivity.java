    public static final String S_count ="setting_count";
    public static final String S_user ="setting_user";
    private void preferencesSettingTest(){


        //get preference
        PreferenceActivity pre;
        SharedPreferences sp = PreferenceManager.getDefaultSharedPreferences(this);
        int iCount = sp.getInt(S_count,0);
        String suser = sp.getString(S_user,"user");
        Log.d("Count:",""+iCount);
        iCount++;
        SharedPreferences.Editor se = sp.edit();
        se.putInt(S_count, iCount);
        se.putString(S_user,suser+iCount);
        se.commit();
    }
